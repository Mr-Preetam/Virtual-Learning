<p id="detail" class="fs-2">Java Course Syllabus</p>

<p>
<b>Module 1 (10 Hours)</b>
Features of Java, Data types, operators & expressions, control structures, arrays, Classes, objects &
methods, constructors, garbage collection, access qualifiers, Overloading, String Handling – string
operations, character extraction, string comparison, searching and modifying strings, String Buffer,
String Builder, Packages, Interfaces, Wrapper classes, Static variables and methods.
</p>
<p>
<b>Module 2 (14 Hours)</b>
Inheritance: single and multilevel inheritance, method overriding, abstract class, use of super and final
keywords. Exception Handling: Exception types, uncaught exceptions, multiple catch clauses, nested try
statements, built-in exceptions, creating your own exceptions. Multithreading: Java thread model,
creating multiple threads, thread priorities, synchronization, inter-thread communication, suspending,
resuming and stopping threads; Familiarity with Java Collection Framework.

I/O Streams: Console I/O, Files I/O – Byte Streams, Character Streams, Object Serialization; Socket
Programming: TCP Socket, Datagram Socket.
</p>
<p>
<b>Module 3 (12 Hours)</b>
JDBC programming: JDBC Drivers, Creating connection, executing queries and stored procedures,
handling database transactions.
GUI Development: AWT Classes, Window fundamentals, working with graphics, working with color &
fonts. Event handling in Java, Delegation Event Model, Swing Package: JFrame, JPanel, swing GUI
controls, layout managers, working with menus, Introduction to JavaFX
</p> 